const mongoose = require('mongoose')

const departmentSchema = new mongoose.Schema(
  {
    name: String
  },
  {
    timestamps: true,
    toObject: {
      transform: (doc, ret, options) => {
        ret.id = ret._id
        delete ret._id
        delete ret.__v
        return ret
      }
    }
  }
)

module.exports = mongoose.model('Department', departmentSchema)